void main(){
  //there are 7 data type in dart
  // Number, string, boolean, lists, maps , runes, symbols.

  // Number data have 2 types: integer and double

  // Dart Number:
  // integer Number

  int intNumber = 12;
  print(intNumber);

  //double Number
  double doubleNumber = 12.50;
  print(doubleNumber);

  // Dart String
  // String can be written in single quote or double quote.
  // String can carry name, address or special character.

  String singleString = 'Abu Bakar';
  String doubleString = "Abu Bakar";
  print(singleString);
  print(doubleString);

  // Dart Boolean
  // Boolean have only 2 value its true or false.
  // the bool keyword use to detonate boolean types.

  bool negative = false;
  bool positive = true;

  print(negative);
  print(positive);

}