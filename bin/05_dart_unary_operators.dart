void main(){
  // Dart Operators
// Unary Operators

  int u1 = 10;
  int u2 = 20;
  int u3 = 30;
  int u4 = 40;

  // Prefix (++x) - it increment the value of operand.
  print(++u2);
  print(++u2);
  print(++u2);
  print(++u2);
  print(++u2);

  print("===============");

  // Postfix (x++) -  it will returns the actual value of operand before increment.
  print(u1++);
  print(u1++);
  print(u1++);
  print(u1++);
  print(u1++);

  print("===============");

  // Prefix (--x) - it decrement the value of operand.
  print(--u3);
  print(--u3);
  print(--u3);
  print(--u3);
  print(--u3);

  print("===============");

  // Postfix (x--) -  it will returns the actual value of operand before decrement.
  print(u4--);
  print(u4--);
  print(u4--);
  print(u4--);
  print(u4--);

}